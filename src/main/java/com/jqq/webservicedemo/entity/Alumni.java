package com.jqq.webservicedemo.entity;



public class Alumni {
    int id;
    String name;
    String sex;
    String birthday;
    String inSchoolDate;
    String outSchoolDate;
    String workingCity;
    String workingUnit;
    String job;
    String phoneNum;
    String email;
    String wechat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getInSchoolDate() {
        return inSchoolDate;
    }

    public void setInSchoolDate(String inSchoolDate) {
        this.inSchoolDate = inSchoolDate;
    }

    public String getOutSchoolDate() {
        return outSchoolDate;
    }

    public void setOutSchoolDate(String outSchoolDate) {
        this.outSchoolDate = outSchoolDate;
    }

    public String getWorkingCity() {
        return workingCity;
    }

    public void setWorkingCity(String workingCity) {
        this.workingCity = workingCity;
    }

    public String getWorkingUnit() {
        return workingUnit;
    }

    public void setWorkingUnit(String workingUnit) {
        this.workingUnit = workingUnit;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWechat() {
        return wechat;
    }

    public void setWechat(String wechat) {
        this.wechat = wechat;
    }
}
